/**
 * 描述: 位置信息
 *
 * @author zhourao
 * @create 2021-06-11 10:58 下午
 */
public class Position {

    private int x, y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
